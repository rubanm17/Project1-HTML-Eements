print("Central Limit Theorem (CLT) Examples:")


print("Example 1: Student Heights")
print("Population: Heights of all students in a school.")
print("Result:")    
print("   The distribution of the average heights will be approximately normal.")

print("Example 2: Dice Rolls")

print("Population: Outcomes of a die (1, 2, 3, 4, 5, 6).")
print("Result:")
print("   Even though die outcomes are uniformly distributed,")
print("   the averages form a bell-shaped normal distribution.")


print("Example 3: Customer Spending")

print("Population: Amount spent by customers in a store.")
print("Result:")
print("   The sample means become normally distributed,   ")
print("   even if customer spending is highly skewed.")

print("Example 4: Manufacturing Process")

print("Population: Lengths of screws produced in a factory.")
print("Result:")
print("   The distribution of average lengths becomes normal,")
print("   helping engineers monitor product quality.")


print("Example 5: Exam Scores")

print("Population: Scores of all students in an exam.")
print("Result:")
print("   The distribution of sample averages becomes approximately normal,")
print("  even if individual scores are not normally distributed.")

